// [ ] Faça um programa que encontre a raiz quadrada de um número, multiplique o resultado por 10 e divida por 33

const num1 = 10;
const raiz = Math.sqrt(num1)
const mult = raiz * 10;
const divisao = mult / 33;

console.log(`A raiz quadrada de ${num1} multiplicada por 10 e dividida por 33 é: [${divisao}]`);

